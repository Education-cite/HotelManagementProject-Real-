    private void showBooking() {
        model = new DefaultTableModel();

        model.addColumn("BNumber");//Number=model table column name
        model.addColumn("Room_code");
        model.addColumn("Customer_code");
        model.addColumn("CheackIn");
        model.addColumn("Cheackout");
        model.addColumn("Duration");
        model.addColumn("Cost");
        String sql = "select * from Booking";

        try {
            Connection con = DatabaseConnection.getDBConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {

                String bid = rs.getString("BNum"); //Bnum=sql table column name
                String room = rs.getString("RoomCode");
                String customer = rs.getString("custCode");
                String date = rs.getString("CheackIn");
                String date1 = rs.getString("Cheackout");
                String duration = rs.getString("Duration");
                String cost = rs.getString("Cost");

                Object[] rowData = {bid, room, customer, date,date1, duration, cost};
                model.addRow(rowData);

            }
            Bookinglist.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }


  

